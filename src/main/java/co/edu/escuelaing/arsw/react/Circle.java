package co.edu.escuelaing.arsw.react;

/**
 *
 * @author juaco
 */
public class Circle {
    String posx ;
    String posy ;

    public Circle(String posx, String posy) {
        this.posx = posx;
        this.posy = posy;
    }

    public String getPosx() {
        return posx;
    }

    public String getPosy() {
        return posy;
    }

    public void setPosx(String posx) {
        this.posx = posx;
    }

    public void setPosy(String posy) {
        this.posy = posy;
    }
    
    
}
